// Write a code to display simple interest (Formula S.I. = P × R × T)

var P = 10000;        // principal amount in rs
var R = 0.05;        // rate of interest 5%
var T = 2;           // time in years
var SI;

SI = P*R*T;
console.log ("P is " + P + " rs");
console.log ("R is " + R + " %");
console.log ("T is " + T + " years");
console.log ("The value of Simple Interest is " + SI + " rs");


